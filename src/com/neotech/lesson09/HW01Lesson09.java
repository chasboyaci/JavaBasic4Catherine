package com.neotech.lesson09;

public class HW01Lesson09 {

	public static void main(String[] args) {

		// 1. Print numbers from 1 to 50 that are divisible
		// by 3

		int num = 1;

		while (num <= 50) {
			if (num % 3 == 0)

			{
				System.out.println(num);
			}

			num++;
		}

	}

}
